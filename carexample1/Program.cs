namespace carClassExample
{

    // Class Declaration
    public class car
    {

        // Instance Variables
        String name;
        String brand;
        int model;
        String color;

        // Constructor Declaration of Class
        public car(String name, String brand,
                      int model, String color)
        {
            this.name = name;
            this.brand = brand;
            this.model = model;
            this.color = color;
            




        }
    }
}